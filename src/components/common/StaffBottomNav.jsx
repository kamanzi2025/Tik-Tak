export default function StaffBottomNav({ newOrderCount = 0 }) {
  const location = useLocation()

  const tabs = [
    { to: '/staff', label: 'Orders', icon: OrdersIcon, exact: true, badge: newOrderCount },
    { to: '/staff/menu', label: 'Menu', icon: MenuIcon },
    { to: '/staff/profile', label: 'Restaurant', icon: ProfileIcon },
    { to: '/staff/sales', label: 'Sales', icon: SalesIcon },
    { to: '/staff/settings', label: 'Settings', icon: SettingsIcon },
  ]

  return (
    <nav className="fixed bottom-0 left-0 right-0 bg-white border-t border-gray-200 flex z-50">
      {tabs.map(({ to, label, icon: Icon, exact, badge }) => {
        const active = exact ? location.pathname === to : location.pathname.startsWith(to)
        return (
          <NavLink
            key={to}
            to={to}
            className="flex-1 flex flex-col items-center justify-center py-2 min-h-[56px] relative"
          >
            <span className={active ? 'text-staff' : 'text-gray-400'}>
              <div className="relative inline-block">
                <Icon />
                {badge > 0 && (
                  <span className="absolute -top-1 -right-1 bg-red-500 text-white text-[10px] font-bold rounded-full w-4 h-4 flex items-center justify-center">
                    {badge > 9 ? '9+' : badge}
                  </span>
                )}
              </div>
            </span>
            <span className={`text-[10px] mt-0.5 font-medium ${active ? 'text-staff' : 'text-gray-400'}`}>
              {label}
            </span>
          </NavLink>
        )
      })}
    </nav>
  )
}
